import pandas as pd

df=pd.read_csv("/content/Salary_Dataset_DataScienceLovers.csv")
df

df.info()

df.isnull().sum()

company_name_mode = df['Company Name'].mode()[0]
df['Company Name'].fillna(company_name_mode, inplace=True)

print("Null values after imputation:")
print(df.isnull().sum())

from sklearn.preprocessing import LabelEncoder

# Identify categorical columns (object dtype)
categorical_cols = df.select_dtypes(include='object').columns

# Initialize LabelEncoder
label_encoder = LabelEncoder()

# Apply Label Encoding to each categorical column
for col in categorical_cols:
    df[col] = label_encoder.fit_transform(df[col])

print("DataFrame after Label Encoding (first 5 rows):")
print(df.head())

X = df.drop('Salary', axis=1)
y = df['Salary']

print("Input features (X) shape:", X.shape)
print("Target variable (y) shape:", y.shape)
print("\nFirst 5 rows of X:")
print(X.head())
print("\nFirst 5 values of y:")
print(y.head())

from sklearn.model_selection import train_test_split

X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.25, random_state=42)

print("X_train shape:", X_train.shape)
print("X_test shape:", X_test.shape)
print("y_train shape:", y_train.shape)
print("y_test shape:", y_test.shape)

"""### Consolidated Model Training and Evaluation

To streamline the process, let's train and evaluate several regression models in a single loop. This approach allows for easier comparison of their performance.

We will include:
1.  **Linear Regression**
2.  **Support Vector Regressor (SVR)**
3.  **K-Nearest Neighbors (KNN) Regressor**
4.  **Decision Tree Regressor**
5.  **Random Forest Regressor**

_Note: Naive Bayes is typically used for classification tasks and is not directly applicable to this regression problem._
"""

from sklearn.linear_model import LinearRegression
from sklearn.svm import SVR
from sklearn.neighbors import KNeighborsRegressor
from sklearn.tree import DecisionTreeRegressor
from sklearn.ensemble import RandomForestRegressor
from sklearn.metrics import mean_absolute_error, r2_score
from sklearn.preprocessing import StandardScaler
from sklearn.pipeline import make_pipeline

# Define a list of models to evaluate
models = {
    'Linear Regression': LinearRegression(),
    'SVR': make_pipeline(StandardScaler(), SVR(kernel='rbf', C=1.0, epsilon=0.2)), # SVR with scaling
    'KNN Regressor': KNeighborsRegressor(n_neighbors=5),
    'Decision Tree Regressor': DecisionTreeRegressor(random_state=42),
    'Random Forest Regressor': RandomForestRegressor(n_estimators=100, random_state=42, n_jobs=-1)
}

results = {}

print("Starting consolidated model training and evaluation...")

for name, model in models.items():
    print(f"\nTraining {name}...")

    # Train the model
    model.fit(X_train, y_train)

    # Make predictions
    y_pred = model.predict(X_test)

    # Evaluate the model
    mae = mean_absolute_error(y_test, y_pred)
    r2 = r2_score(y_test, y_pred)

    results[name] = {'MAE': mae, 'R-squared': r2}

    print(f"{name} - Mean Absolute Error: {mae:.2f}")
    print(f"{name} - R-squared: {r2:.2f}")

print("\n--- Model Performance Summary ---")
for name, metrics in results.items():
    print(f"{name}:")
    print(f"  MAE: {metrics['MAE']:.2f}")
    print(f"  R-squared: {metrics['R-squared']:.2f}")

import matplotlib.pyplot as plt
import seaborn as sns
import pandas as pd

# Convert results dictionary to DataFrame for easier plotting
results_df = pd.DataFrame.from_dict(results, orient='index')

# Sort by MAE for better visualization
results_df_sorted_mae = results_df.sort_values(by='MAE', ascending=True)

plt.figure(figsize=(12, 6))
sns.barplot(x=results_df_sorted_mae.index, y='MAE', data=results_df_sorted_mae, hue=results_df_sorted_mae.index, palette='viridis', legend=False)
plt.title('Model Comparison: Mean Absolute Error (MAE)')
plt.xlabel('Model')
plt.ylabel('Mean Absolute Error')
plt.xticks(rotation=45, ha='right')
plt.tight_layout()
plt.show()

# Sort by R-squared for better visualization
results_df_sorted_r2 = results_df.sort_values(by='R-squared', ascending=False)

plt.figure(figsize=(12, 6))
sns.barplot(x=results_df_sorted_r2.index, y='R-squared', data=results_df_sorted_r2, hue=results_df_sorted_r2.index, palette='magma', legend=False)
plt.title('Model Comparison: R-squared')
plt.xlabel('Model')
plt.ylabel('R-squared')
plt.xticks(rotation=45, ha='right')
plt.tight_layout()
plt.show()

