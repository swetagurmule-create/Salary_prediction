# Identify the best model based on MAE from the results_df
results_df = pd.DataFrame.from_dict(results, orient='index')
best_model_name = results_df.sort_values(by='MAE', ascending=True).index[0]

print(f"Identified best model (based on MAE): {best_model_name}")

# Re-instantiate the best model with its original parameters
# This ensures we save a fresh, correctly fitted instance of the best model.
if best_model_name == 'Linear Regression':
    best_model_instance = LinearRegression()
elif best_model_name == 'SVR':
    best_model_instance = make_pipeline(StandardScaler(), SVR(kernel='rbf', C=1.0, epsilon=0.2))
elif best_model_name == 'KNN Regressor':
    best_model_instance = KNeighborsRegressor(n_neighbors=5)
elif best_model_name == 'Decision Tree Regressor':
    best_model_instance = DecisionTreeRegressor(random_state=42)
elif best_model_name == 'Random Forest Regressor':
    best_model_instance = RandomForestRegressor(n_estimators=100, random_state=42, n_jobs=-1)
else:
    raise ValueError(f"Unknown model name: {best_model_name}")

print(f"Refitting {best_model_name} for saving...")
# Fit the model to the training data
best_model_instance.fit(X_train, y_train)

# Define the filename for the pickled model
# Replace spaces and hyphens for a valid filename
filename = f'{best_model_name.replace(" ", "_").replace("-", "_")}.pkl'

# Save the fitted model using pickle
with open(filename, 'wb') as file:
    pickle.dump(best_model_instance, file)

print(f"The best model ({best_model_name}) has been saved as '{filename}'.")
print("Downloading the model file...")

